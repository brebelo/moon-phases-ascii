import math
   
ascii_3p = """ 
               ....................\n
            ........................:#\n
          ...............................:#\n
         ..............................:##\n
         ...............................:#\n
            .........................:#\n
                ....................\n
 
"""
ascii_0p = """

               ....................\n
            ...........................\n
         .................................\n
        ...................................\n
         .................................\n
            ............................\n
               ....................\n

"""

lunes_phases = [
        ascii_0p,
        ascii_3p
    ]



def lune(illumination):
    index = illumination / 3
    index_int = int(index)
    index_int = max(0, min(index_int, len(lunes_phases) - 1))

    return lunes_phases[index_int]

print(lune(15))